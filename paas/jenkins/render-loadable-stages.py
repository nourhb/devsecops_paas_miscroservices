#!/usr/bin/env python3
"""Emit Jenkins loadable stages: helpers + vars + runPaasDeploy closure (June 17 layout)."""
from __future__ import annotations

import sys
from pathlib import Path

BUNDLE_MARKER = "STAGES_BUNDLE_VERSION=paas-monolithic-runPaasDeploy-20260618"


def main() -> int:
    root = Path(__file__).resolve().parent
    main_path = root / "Jenkinsfile.paas-deploy"
    stages_path = root / "Jenkinsfile.paas-deploy-stages.groovy"
    if not main_path.is_file():
        print(f"ERROR: missing {main_path}", file=sys.stderr)
        return 1
    if not stages_path.is_file():
        print(f"ERROR: missing {stages_path}", file=sys.stderr)
        return 1

    text = main_path.read_text(encoding="utf-8").replace("\r\n", "\n")
    lines = text.splitlines(keepends=True)
    try:
        vars_start = next(i for i, line in enumerate(lines) if line.startswith("def agentLabel = "))
        body_start = next(i for i, line in enumerate(lines) if line.startswith("def runPaasDeploy = {"))
    except StopIteration:
        print("ERROR: Jenkinsfile missing def agentLabel / def runPaasDeploy", file=sys.stderr)
        return 1

    helpers = "".join(lines[:vars_start])
    vars_block = "".join(lines[vars_start:body_start])
    stages_body = stages_path.read_text(encoding="utf-8").replace("\r\n", "\n").rstrip()

    output = (
        "// generated by render-loadable-stages.py (helpers + vars + runPaasDeploy closure)\n"
        f"// {BUNDLE_MARKER}\n"
        f"{helpers}"
        f"{vars_block}"
        "def runPaasDeploy = {\n"
        f"{stages_body}\n"
        "}\n\n"
        "return this\n"
    )

    if "def runPaasDeploy = {" not in output:
        print("ERROR: generated output missing runPaasDeploy closure", file=sys.stderr)
        return 1
    if 'stage("Step 12 —' not in output:
        print("ERROR: generated output missing Step 12 stage", file=sys.stderr)
        return 1
    if "def paasDeployInit" in output or "def runPaasStep01" in output:
        print("ERROR: generated output must not split into paasDeployInit/runPaasStepNN", file=sys.stderr)
        return 1

    sys.stdout.buffer.write(output.encode("utf-8"))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
