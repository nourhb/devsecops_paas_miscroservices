#!/usr/bin/env node
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const argIn = process.argv[2];
const argOut = process.argv[3];
const inputPath = argIn ? resolve(process.cwd(), argIn) : join(root, ".env");
const outputPath = argOut ? resolve(process.cwd(), argOut) : join(root, "docker-compose.env");
const KEY_RE = /^\s*([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)$/;
function parseLooseEnv(text) {
    const lines = text.split(/\r?\n/);
    const out = [];
    let current = null;
    const flush = () => {
        if (!current)
            return;
        out.push([current.key, current.parts.join("\n")]);
        current = null;
    };
    for (const line of lines) {
        const t = line.trimEnd();
        if (/^\s*#/.test(t) || /^\s*$/.test(t)) {
            continue;
        }
        const m = t.match(KEY_RE);
        if (m) {
            flush();
            current = { key: m[1], parts: [m[2] == null ? "" : m[2]] };
            continue;
        }
        if (current) {
            current.parts.push(t);
        }
    }
    flush();
    return out;
}
function escapeForComposeLine(value) {
    const needsQuote = /[\r\n]/.test(value) ||
        /["\\]/.test(value) ||
        /\s/.test(value) ||
        /[#]/.test(value) ||
        /\$/.test(value) ||
        value === "";
    if (!needsQuote) {
        return `${value}`;
    }
    const escaped = value
        .replace(/\\/g, "\\\\")
        .replace(/"/g, '\\"')
        .replace(/\r\n/g, "\n")
        .replace(/\n/g, "\\n")
        .replace(/\r/g, "\\n")
        .replace(/\$/g, "$$$$");
    return `"${escaped}"`;
}
function main() {
    if (!existsSync(inputPath)) {
        console.error(`Missing ${inputPath}; copy .env.example first.`);
        process.exit(1);
    }
    const text = readFileSync(inputPath, "utf8");
    const entries = parseLooseEnv(text);
    const header = [
        "# Generated by scripts/flatten-env-for-compose.mjs — do not edit this file by hand.",
        "# Source: frontend/.env  |  Regenerate: npm run env:compose",
        "#",
        "# If paas-frontend runs in Docker (paas/docker-compose.yml):",
        "# - DATABASE_URL: use hostname `postgres` (compose DB) or `host.docker.internal` — never `localhost` (that is the container itself).",
        "# - paas/docker-compose.yml may override DATABASE_URL; align .env with that or remove the override.",
        "# - APP_BASE_URL: port 3000 (image maps 3000:3000), not 3001.",
        "# - KUBE_CONFIG_PATH: path valid inside the container if you mount a kubeconfig; disable KUBERNETES_ENABLED if you only use Swarm.",
        "# - Trivy on Swarm my-stack is typically :4954, not :30613.",
        "# - Fix HARBOR_REGISTRY (not REGITRY); drop unused REGITRY_* keys unless your tooling uses them.",
        "#",
        "",
    ].join("\n");
    const body = entries.map(([k, v]) => `${k}=${escapeForComposeLine(v)}`).join("\n");
    writeFileSync(outputPath, `${header}${body}\n`, "utf8");
    console.log(`Wrote ${outputPath} (${entries.length} variables)`);
}
main();
