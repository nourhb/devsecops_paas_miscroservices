import { env } from "@/server/config/env";
export type BuildProvider = "jenkins" | "tekton";
export type BuildProfile = "node" | "python" | "java" | "static" | "custom";
export type BuildMode = "platform-template" | "custom-dockerfile";
export interface BuildPlanInput {
    language?: string | null;
    gitRepositoryUrl?: string | null;
    autoGenerateDockerfile?: boolean | null;
}
export interface ResolvedBuildPlan {
    provider: BuildProvider;
    profile: BuildProfile;
    mode: BuildMode;
    templateName: string;
    templateVersion: string;
    detectionReason: string;
    zeroConfig: boolean;
}
function includesAny(value: string, needles: string[]): boolean {
    return needles.some((needle) => value.includes(needle));
}
function detectProfile(input: BuildPlanInput): {
    profile: BuildProfile;
    detectionReason: string;
} {
    const language = String(input.language ?? "").trim().toLowerCase();
    const repo = String(input.gitRepositoryUrl ?? "").trim().toLowerCase();
    const combined = `${language} ${repo}`.trim();
    if (includesAny(combined, ["angular"])) {
        return {
            profile: "static",
            detectionReason: language ? `Detected Angular from stack "${input.language}" (nginx static on port 80).` : "Detected Angular-style repository metadata (nginx static on port 80)."
        };
    }
    if (includesAny(combined, ["next", "nestjs", "nest", "express"])) {
        return {
            profile: "node",
            detectionReason: language ? `Detected server-side Node stack "${input.language}".` : "Detected Node server repository metadata."
        };
    }
    if (includesAny(combined, ["react", "vue", "vite", "spa"]) && !includesAny(combined, ["nestjs", "nest", "express", "next"])) {
        return {
            profile: "static",
            detectionReason: language ? `Detected static web app "${input.language}" (build → nginx on port 80).` : "Detected static SPA repository metadata (nginx on port 80)."
        };
    }
    if (includesAny(combined, ["node", "javascript", "typescript"])) {
        return {
            profile: "node",
            detectionReason: language ? `Detected from stack "${input.language}".` : "Detected Node-style repository metadata."
        };
    }
    if (includesAny(combined, ["python", "django", "flask", "fastapi", "poetry", "pip"])) {
        return {
            profile: "python",
            detectionReason: language ? `Detected from stack "${input.language}".` : "Detected Python-style repository metadata."
        };
    }
    if (includesAny(combined, ["java", "spring", "maven", "gradle", "kotlin"])) {
        return {
            profile: "java",
            detectionReason: language ? `Detected from stack "${input.language}".` : "Detected JVM-style repository metadata."
        };
    }
    if (includesAny(combined, ["static", "html", "nginx", "frontend", "spa"])) {
        return {
            profile: "static",
            detectionReason: language ? `Detected from stack "${input.language}".` : "Detected static-site repository metadata."
        };
    }
    return {
        profile: "custom",
        detectionReason: language
            ? `No managed template matched stack "${input.language}", using custom mode.`
            : "No managed template matched the repository metadata, using custom mode."
    };
}
function templateNameFor(profile: BuildProfile, mode: BuildMode): string {
    if (mode === "custom-dockerfile") {
        return "repo-dockerfile";
    }
    switch (profile) {
        case "node":
            return "node-service";
        case "python":
            return "python-service";
        case "java":
            return "java-service";
        case "static":
            return "static-site";
        default:
            return "custom";
    }
}
export function resolveBuildProvider(): BuildProvider {
    return env.BUILD_BACKEND;
}
export function resolveBuildPlan(input: BuildPlanInput): ResolvedBuildPlan {
    const detected = detectProfile(input);
    const provider = resolveBuildProvider();
    const autoGenerateDockerfile = input.autoGenerateDockerfile !== false;
    const mode: BuildMode = detected.profile === "custom" || !autoGenerateDockerfile ? "custom-dockerfile" : "platform-template";
    return {
        provider,
        profile: detected.profile,
        mode,
        templateName: templateNameFor(detected.profile, mode),
        templateVersion: env.BUILD_TEMPLATE_VERSION,
        detectionReason: detected.detectionReason,
        zeroConfig: mode === "platform-template"
    };
}
